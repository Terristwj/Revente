            foreground,
        ];

        const battle = {
            initiated: false,
        };

        function animate() {
            const animationId = window.requestAnimationFrame(animate);
            renderables.forEach((renderable) => {
                renderable.draw();
            });

            let moving = true;
            player.animate = false;

            if (battle.initiated) return;

            // activate a battle
            if (
                keys.w.pressed ||
                keys.a.pressed ||
                keys.s.pressed ||
                keys.d.pressed
            ) {
                for (let i = 0; i < battleZones.length; i++) {
                    const battleZone = battleZones[i];
                    const overlappingArea =
                        (Math.min(
                            player.position.x + player.width,
                            battleZone.position.x + battleZone.width
                        ) -
                            Math.max(
                                player.position.x,
                                battleZone.position.x
                            )) *
                        (Math.min(
                            player.position.y + player.height,
                            battleZone.position.y + battleZone.height
                        ) -
                            Math.max(player.position.y, battleZone.position.y));
                    if (
                        rectangularCollision({
                            rectangle1: player,
                            rectangle2: battleZone,
                        }) &&
                        overlappingArea > (player.width * player.height) / 2 &&
                        Math.random() < 0.01
                    ) {
                        // deactivate current animation loop
                        window.cancelAnimationFrame(animationId);

                        audio.Map.stop();
                        audio.initBattle.play();
                        audio.battle.play();

                        battle.initiated = true;
                        gsap.to("#overlappingDiv", {
                            opacity: 1,
                            repeat: 3,
                            yoyo: true,
                            duration: 0.4,
                            onComplete() {
                                gsap.to("#overlappingDiv", {
                                    opacity: 1,
                                    duration: 0.4,
                                    onComplete() {
                                        // activate a new animation loop
                                        initBattle();
                                        animateBattle();
                                        gsap.to("#overlappingDiv", {
                                            opacity: 0,
                                            duration: 0.4,
                                        });
                                    },
                                });
                            },
                        });
                        break;
                    }
                }
            }

            if (keys.w.pressed && lastKey === "w") {
                player.animate = true;
                player.image = player.sprites.up;

                checkForCharacterCollision({
                    characters,
                    player,
                    characterOffset: { x: 0, y: movementSpeed },
                });

                for (let i = 0; i < boundaries.length; i++) {
                    const boundary = boundaries[i];
                    if (
                        rectangularCollision({
                            rectangle1: player,
                            rectangle2: {
                                ...boundary,
                                position: {
                                    x: boundary.position.x,
                                    y: boundary.position.y + movementSpeed,
                                },
                            },
                        })
                    ) {
                        moving = false;
                        break;
                    }
                }

                if (moving)
                    movables.forEach((movable) => {
                        movable.position.y += movementSpeed;
                    });
            } else if (keys.a.pressed && lastKey === "a") {
                player.animate = true;
                player.image = player.sprites.left;

                checkForCharacterCollision({
                    characters,
                    player,
                    characterOffset: { x: movementSpeed, y: 0 },
                });

                for (let i = 0; i < boundaries.length; i++) {
                    const boundary = boundaries[i];
                    if (
                        rectangularCollision({
                            rectangle1: player,
                            rectangle2: {
                                ...boundary,
                                position: {
                                    x: boundary.position.x + movementSpeed,
                                    y: boundary.position.y,
                                },
                            },
                        })
                    ) {
                        moving = false;
                        break;
                    }
                }

                if (moving)
                    movables.forEach((movable) => {
                        movable.position.x += movementSpeed;
                    });
            } else if (keys.s.pressed && lastKey === "s") {
                player.animate = true;
                player.image = player.sprites.down;

                checkForCharacterCollision({
                    characters,
                    player,
                    characterOffset: { x: 0, y: -movementSpeed },
                });

                for (let i = 0; i < boundaries.length; i++) {
                    const boundary = boundaries[i];
                    if (
                        rectangularCollision({
                            rectangle1: player,
                            rectangle2: {
                                ...boundary,
                                position: {
                                    x: boundary.position.x,
                                    y: boundary.position.y - movementSpeed,
                                },
                            },
                        })
                    ) {
                        moving = false;
                        break;
                    }
                }

                if (moving)
                    movables.forEach((movable) => {
                        movable.position.y -= movementSpeed;
                    });
            } else if (keys.d.pressed && lastKey === "d") {
                player.animate = true;
                player.image = player.sprites.right;

                checkForCharacterCollision({
                    characters,
                    player,
                    characterOffset: { x: -movementSpeed, y: 0 },
                });

                for (let i = 0; i < boundaries.length; i++) {
                    const boundary = boundaries[i];
                    if (
                        rectangularCollision({
                            rectangle1: player,
                            rectangle2: {
                                ...boundary,
                                position: {
                                    x: boundary.position.x - movementSpeed,
                                    y: boundary.position.y,
                                },
                            },
                        })
                    ) {
                        moving = false;
                        break;
                    }
                }

                if (moving)
                    movables.forEach((movable) => {
                        movable.position.x -= movementSpeed;
                    });
            }
        }
        // animate()

        let lastKey = "";
        window.addEventListener("keydown", (e) => {
            if (player.isInteracting) {
                switch (e.key) {
                    case "Enter": {
                        player.interactionAsset.dialogueIndex++;

                        const { dialogueIndex, dialogue } =
                            player.interactionAsset;
                        if (dialogueIndex <= dialogue.length - 1) {
                            document.querySelector(
                                "#characterDialogueBox"
                            ).innerHTML =
                                player.interactionAsset.dialogue[dialogueIndex];
                            return;
                        }

                        // finish conversation
                        player.isInteracting = false;
                        player.interactionAsset.dialogueIndex = 0;
                        document.querySelector(
                            "#characterDialogueBox"
                        ).style.display = "none";

                        break;
                    }
                }
                return;
            }

            switch (e.key) {
                case "Enter": {
                    if (!player.interactionAsset) return;

                    // beginning the conversation
                    const firstMessage = player.interactionAsset.dialogue[0];
                    document.querySelector("#characterDialogueBox").innerHTML =
                        firstMessage;
                    document.querySelector(
                        "#characterDialogueBox"
                    ).style.display = "flex";
                    player.isInteracting = true;
                    break;
                }
                case "w": {
                    keys.w.pressed = true;
                    lastKey = "w";
                    break;
                }
                case "a": {
                    keys.a.pressed = true;
                    lastKey = "a";
                    break;
                }

                case "s": {
                    keys.s.pressed = true;
                    lastKey = "s";
                    break;
                }

                case "d": {
                    keys.d.pressed = true;
                    lastKey = "d";
                    break;
                }
            }
        });

        window.addEventListener("keyup", (e) => {
            switch (e.key) {
                case "w":
                    keys.w.pressed = false;
                    break;
                case "a":
                    keys.a.pressed = false;
                    break;
                case "s":
                    keys.s.pressed = false;
                    break;
                case "d":
                    keys.d.pressed = false;
                    break;
            }
        });

        let clicked = false;
        addEventListener("click", () => {
            if (!clicked) {
                audio.Map.play();
                clicked = true;
            }
        });

        const battleBackgroundImage = new Image();
        battleBackgroundImage.src = "./img/2d_game/battleBackground.png";
        const battleBackground = new Sprite({
            position: {
                x: 0,
                y: 0,
            },
            image: battleBackgroundImage,
        });

        let draggle;
        let emby;
        let renderedSprites;
        let battleAnimationId;
        let queue;

        function initBattle() {
            document.querySelector("#userInterface").style.display = "block";
            document.querySelector("#dialogueBox").style.display = "none";
            document.querySelector("#enemyHealthBar").style.width = "100%";
            document.querySelector("#playerHealthBar").style.width = "100%";
            document.querySelector("#attacksBox").replaceChildren();

            draggle = new Monster(monsters.Draggle);
            emby = new Monster(monsters.Emby);
            renderedSprites = [draggle, emby];
            queue = [];

            emby.attacks.forEach((attack) => {
                const button = document.createElement("button");
                button.innerHTML = attack.name;
                document.querySelector("#attacksBox").append(button);
            });

            // our event listeners for our buttons (attack)
            document.querySelectorAll("button").forEach((button) => {
                button.addEventListener("click", (e) => {
                    const selectedAttack = attacks[e.currentTarget.innerHTML];
                    // console.log(selectedAttack);
                    emby.attack({
                        attack: selectedAttack,
                        recipient: draggle,
                        renderedSprites,
                    });

                    if (draggle.health <= 0) {
                        queue.push(() => {
                            draggle.faint();
                        });
                        queue.push(() => {
                            // fade back to black
                            gsap.to("#overlappingDiv", {
                                opacity: 1,
                                onComplete: () => {
                                    cancelAnimationFrame(battleAnimationId);
                                    animate();
                                    document.querySelector(
                                        "#userInterface"
                                    ).style.display = "none";

                                    gsap.to("#overlappingDiv", {
                                        opacity: 0,
                                    });

                                    battle.initiated = false;
                                    audio.Map.play();
                                },
                            });
                        });
                    }

                    // draggle or enemy attacks right here
                    const randomAttack =
                        draggle.attacks[
                            Math.floor(Math.random() * draggle.attacks.length)
                        ];

                    queue.push(() => {
                        draggle.attack({
                            attack: randomAttack,
                            recipient: emby,
                            renderedSprites,
                        });

                        if (emby.health <= 0) {
                            queue.push(() => {
                                emby.faint();
                            });

                            queue.push(() => {
                                // fade back to black
                                gsap.to("#overlappingDiv", {
                                    opacity: 1,
                                    onComplete: () => {
                                        cancelAnimationFrame(battleAnimationId);
                                        animate();
                                        document.querySelector(
                                            "#userInterface"
                                        ).style.display = "none";

                                        gsap.to("#overlappingDiv", {
                                            opacity: 0,
                                        });

                                        battle.initiated = false;
                                        audio.Map.play();
                                    },
                                });
                            });
                        }
                    });
                });

                button.addEventListener("mouseenter", (e) => {
                    const selectedAttack = attacks[e.currentTarget.innerHTML];
                    document.querySelector("#attackType").innerHTML =
                        selectedAttack.type;
                    document.querySelector("#attackType").style.color =
                        selectedAttack.color;
                });
            });
        }

        function animateBattle() {
            battleAnimationId = window.requestAnimationFrame(animateBattle);
            battleBackground.draw();

            // console.log(battleAnimationId);

            renderedSprites.forEach((sprite) => {
                sprite.draw();
            });
        }

        animate();
        // initBattle()
        // animateBattle()

        document
            .querySelector("#dialogueBox")
            .addEventListener("click", (e) => {
                if (queue.length > 0) {
                    queue[0]();
                    queue.shift();
                } else e.currentTarget.style.display = "none";
            });
    },
    methods: {},
};
</script>

